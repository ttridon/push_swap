#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		if (n < (-10))
			ft_putnbr(n / (-10));
	}
	else if (n > 9)
		ft_putnbr(n / 10);
	if (n >= 0)
		ft_putchar(n % 10 + 48);
	else
		ft_putchar(48 - n % 10);
}
